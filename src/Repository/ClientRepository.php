public function findMostPopularVilles()
    {
    $sql = "SELECT v2.adrc, COUNT(*) As howMany
    FROM Client v1
    INNER JOIN Client v2 ON v2.id=v1.id
    GROUP BY v2.adrc
    ";
    $statement = $this->_em->getConnection()->prepare($sql);
    $result = $statement->executeQuery()->fetchAllAssociative();
    return $result;
    }
